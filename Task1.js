/* Exercise: Repetitive tasks with loops
In this exercise, you will practice writing "for" and "while" loops.

Task 1
Write a "for" loop that will perform exactly the same repetitive code as this: 

console.log(1)
console.log(2)
console.log(3)
console.log(4)
console.log(5)
console.log('Counting completed!')

Answer: */

for (var i = 1; i <=5; i++) {
    console.log(i)
}
console.log('Counting completed!')

/*---------------------------------------------------------------------------------------------------------------------*/

/* Task 2 
Write a "for" loop that will perform exactly the same repetitive code as this: 

console.log(5)
console.log(4)
console.log(3)
console.log(2)
console.log(1)
console.log('Countdown finished!')

Answer: */

for (var i = 5; i >= 1; i--) {
    console.log(i)
}
console.log('Countdown finished!')

/*---------------------------------------------------------------------------------------------------------------------*/

/* Task 3
Write a "while" loop that will perform exactly the same repetitive code as this:

console.log(1)
console.log(2)
console.log(3)
console.log(4)
console.log(5)
console.log('Counting completed!')

Answer: */

var i = 1

while  (i <= 5) {
    console.log(i)
    i++
}
console.log('Counting Completed!')

